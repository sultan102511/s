package com.java;

public class Practice4_1to10 {
    public static void main(String[] args) {
        // Write a program to print 1 to 10
        int a = 1;

        while (a <= 10) {
            System.out.println(a);
            a++;
        }

        for (int b = 25; b <= 30; b++ ) {
            System.out.println(b);

        }
    }
}
