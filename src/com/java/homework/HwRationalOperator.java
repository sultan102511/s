package com.java.homework;

public class HwRationalOperator {
    public static void main(String[] args) {
        int a = 1, b = -1;
        System.out.println(a > b); // True
        System.out.println(a < b); // False
        System.out.println(a >= b); // True
        System.out.println(a <= b); // False
        System.out.println(a != b); // True
        System.out.println(a == b); // False
    }
}
