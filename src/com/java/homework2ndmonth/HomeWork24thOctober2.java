package com.java.homework2ndmonth;
//  Java program to find result of 10 + 1 + 9 + 2 + 8 + 3 + 7 + 4 + 6 + 5
public class HomeWork24thOctober2 {
    public static void main(String[] args) {
        int sum = 0;
        for (int num=10; num >=1; num--){
            sum = sum + num;
        }
        System.out.println(sum);
    }
}
