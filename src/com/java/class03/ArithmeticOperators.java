package com.java.class03;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 8;
        System.out.println(num1 / num2);

        // To find out if digit is odd or even
        System.out.println(10 % 2); // Answer - 0
        System.out.println(132727 % 2); // Answer - 1

        // Get last digit of the number
        System.out.println(7 % 10); // Answer - 7
        System.out.println(143 % 10); // Answer - 3

        // Remove the last digit from number
        System.out.println(67 / 10); // Answer 6
        System.out.println(2323 / 10);  // Answer 232
        int p =6;
        int e = 15;
        System.out.println(p%e);


    }
}
