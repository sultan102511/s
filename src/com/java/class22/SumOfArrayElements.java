package com.java.class22;

public class SumOfArrayElements {
    public static void main(String[] args) {
        int num [] = {10,20,30,40,50,60,70,80,90,32,12};
        int b = 0;
        for (int i = 0; i < num.length; i++){
            b=b+num[i];

        }
        System.out.println(b);

    }
}
